<h2>Artist: Ben Banker</h2>
<p class="title">Gouache</p>
<p>2021 2022</p>
<p>An alumnus of the School of the Art Institute of Chicago, Ben Banker presents 'Lamp', a 22" x 30" framed gouache that traversed the American landscape before finding its home in 2022. Created in Madison, WI, and refined amidst the vistas of an East Coast road trip, this piece balances chromatic intensity with serene composition. The artwork’s palette—ranging from profound midnight blues to vibrant yellows—creates a visual equilibrium, leading viewers through a rich narrative told in the universal language of color.</p>
