  <h2>Artist: Ben Banker</h2>
  <p class="title">Gouache on 140 lb cold press paper</p>
  <p>2022 2023</p>
  <p>It gives us immense pleasure to present 'Smoke', a 22" x 30" gouache masterpiece that encapsulates the vivid interplay of light and shadow, a hallmark of Ben Banker's unique style. This piece, originating from the heart of Madison, WI, unfolds a complex narrative of rejuvenation and hope. Over several meticulous months, the artist has woven a tapestry of hues and textures, delivering not just an image but an immersive experience. The artwork invites the viewer to a contemplative space where Japanese artistic influence and personal storytelling converge.</p>
